class App(customtkinter.CTk, Screenshot):
    def __init__(self):
        super().__init__()

        self.title("Grabby")
        self.geometry("650x350")
        self.minsize(650, 350)

        # Initialize windows list and x1, y1 variables
        self.windows = []
        self.x1 = None
        self.y1 = None


